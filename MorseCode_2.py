def decode_bits(bits):
    start_0 = bits.find('0')
    end_0 = bits.find('1', start_0)
    bits = ''.join(bits[x] for x in range(0, len(bits), end_0-start_0))
    return bits.replace('0000000', '   ').replace('111', '-').replace('000', ' ').replace('1', '.').replace('0', '')

def decode_morse(morseCode):
    words = morseCode.strip().split('   ')
    text = ''
    for word in words:
        print(word.replace(' ','_'))
        text += ''.join(MORSE_CODE[key] for key in word.strip().split(' '))
        text += ' '
    return text[:-1]

